for item in chapters:
    chapter_count += 1
    # Check if we've seen this book before, if not, append to list and count books
    if not item[0] in book_names:
        book_names.append(item[0])
        book_count += 1
    # build search pattern string: "*Book Chapter 01.mp3"
    name = "*"+item[0]+" Chapter "+item[1]+".mp3"
    #print(name)
    # Track how many files match the search pattern
    count = 0
    # Store the file names so we can see which ones match
    files = []
    # Search for files that match the pattern, increase count for each one found
    for file in glob.glob(name):
        count += 1
        files.append(file)
    # If more than one file matches this search, don't rename
    if count != 1:
        print(item)
        print(files)
        print("count", count)
    # If final count == 1, then rename
    elif count == 1:
        # Use zfill to add leading zeroes
        prefix = str(chapter_count).zfill(4)
        # New filename
        new_file = "__"+item[0]+" Chapter "+item[1]+".mp3"
        # Final filename
        file_renamed = prefix+new_file
        # Rename if filenames differ
        if file != file_renamed:
            print(file,">",file_renamed)
            os.rename(file, file_renamed)

